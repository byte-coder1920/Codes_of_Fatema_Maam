a=[2,33,2,1,4,3,"rahim",3.2]
a.remove(1) # only value no index directly remove the value
print(a)
a.pop(1)   # deletes according to index
print(a)
a.pop()    #  will delete last value
print(a)
a.clear() # only brackets will remain
print(a)
